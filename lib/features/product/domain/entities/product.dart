class Product {
  final int id;
  final String title;
  final String image;
  final double price;
  final double rating;
  final String category;
  final int reviewCount;


  Product({
    required this.id,
    required this.title,
    required this.image,
    required this.price,
    required this.rating,
    required this.category,
    required this.reviewCount,
  });
}
